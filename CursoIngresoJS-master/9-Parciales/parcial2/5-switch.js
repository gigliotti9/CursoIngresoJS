//Debemos lograr mostrar un mensaje al presionar el botón  'MOSTRAR'.
function Mostrar()
{
	var dia=prompt("ingrese un mes ");
	var minuscula=dia.toLowerCase();

	switch(minuscula){

		case("enero"):
		case("febrero"):

		alert("veranito!!");
		break;

		case("marzo"):
		case("abril"):
		case("mayo"):
		case("junio"):
		case("julio"):
		case("agosto"):
		case("septiembre"):
		case("octubre"):
		case("noviembre"):
		case("diciembre"):

		alert("extraño enero y febrero");

		default:

		
		

	}
		
}

